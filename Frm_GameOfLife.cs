        private void ApplySpeedFromTrckbr()
        {
            int genPerSec = trckBr_Speed.Value;
            //int timerTick = Math.Max(1,trckBr_Speed.Maximum - genPerSec); 
            int timerTick = Math.Max(1, 1000 / genPerSec);
            timer1.Interval = timerTick;
        }

